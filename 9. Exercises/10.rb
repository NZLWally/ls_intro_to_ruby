# Can hash values be arrays? Can you have an array of hashes? (give examples)

# Yes, hash values can be arrays:

hash = {
  dog: ["Ludo", "Fido", "Spot"]
}

p hash

# Yes, you can have an array of hashes:

array = [
  {
    dog1: "Ludo",
    dog2: "Fido"
  },
  {
    cat1: "Molly",
    cat2: "Misty"
  }
]

p array